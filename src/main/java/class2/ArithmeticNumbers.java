package class2;

public class ArithmeticNumbers {
    public static void main(String[] args) {
        double Num1 = 20;
        double Num2 = 10;
        System.out.println("The addition of " + Num1+" and "+Num2+ " is equal to"+(Num1+Num2));
        System.out.println("The subtraction of " + Num1+" and "+Num2+ " is equal to"+(Num1-Num2));
        System.out.println("The multiplication of " + Num1+" and "+Num2+ " is equal to"+(Num1*Num2));
        System.out.println("The division of " + Num1+" and "+Num2+ " is equal to"+(Num1/Num2));






    }
}
