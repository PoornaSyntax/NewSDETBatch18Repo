package youtube;

public class SwitchCase {
    public static void main(String[] args) {
        int n = 6;
        switch(n){
            case 10:
                System.out.println("= 10");
            case 90:
                System.out.println("= 90");
            default:
                System.out.println("none of the above");
            case -3:
                System.out.println("= -3");
        }

    }
}
